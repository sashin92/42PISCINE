#include <unistd.h>

void	ft_print_comb2(void)
{
	int		a;
	int		b;
	char	val[5];

	val[2] = ' ';
	a = 0;
	while (a <= 99)
	{
		val[0] = ((a / 10) + 48);
		val[1] = ((a % 10) + 48);
		b = a + 1;
		while (b <= 99)
		{
			val[3] = ((b / 10) + 48);
			val[4] = ((b % 10) + 48);
			write(1, &val, 5);
			if (!(a == 98 && b == 99))
				write(1, ", ", 2);
			++b;
		}
		++a;
	}
}
