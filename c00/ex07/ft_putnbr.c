#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	num;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	else if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * (-1);
	}
	num = nb % 10 + 48;
	if (nb >= 10)
	{
		nb = nb / 10;
		ft_putnbr(nb);
	}
	write(1, &num, 1);
}
