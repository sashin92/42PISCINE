#include <unistd.h>

void	ft_swap(int *a, int *b);

int		main(void)
{
	int		x;
	int		y;
	char	a;
	char	b;

	x = 3;
	y = 5;
	ft_swap(&x, &y);
	a = x + '0';
	b = y + '0';
	write(1, &a, 1);
	write(1, &b, 1);
}
