void	ft_rev_int_tab(int *tab, int size)
{
	int		temp[size];
	int		i;

	i = 0;
	while (!(i == size))
	{
		temp[i] = *(tab + (size - 1) - i);
		++i;
	}
	i = 0;
	while (!(i == size))
	{
		*(tab + i) = temp[i];
		++i;
	}
}
